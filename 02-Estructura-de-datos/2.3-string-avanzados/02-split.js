// Métodos de transformación - separador

const frase = "manzana,pera,uva,naranja";
const frutas = frase.split(",");
console.log(frutas); // ["manzana", "pera", "uva", "naranja"]

const palabras = "Hola mundo JavaScript".split(" ");
console.log(palabras); // ["Hola", "mundo", "JavaScript"]

const letras = "JavaScript".split("");
console.log(letras); // ["J", "a", "v", "a", "S", "c", "r", "i", "p", "t"]