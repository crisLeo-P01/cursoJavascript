//  ARITMÉTICOS
let a = 5;
let b = 10;

console.log(a + b); // Suma
console.log(a - b); // Resta
console.log(a * b); // Multiplicación
console.log(a / b); // División
console.log(a % b); // 1 (resto)

// COMPARACIÓN
console.log(a == b); // Igualdad
console.log(a != b); // Desigualdad
console.log(a === b); // Igualdad estricta
console.log(a !== b); // Desigualdad estricta
console.log(a > b); // Mayor que
console.log(a < b); // Menor que
console.log(a >= b); // Mayor o igual que
console.log(a <= b); // Menor o igual que

// LOGICOS
let c = true;
let d = false;

console.log(c && d); // true AND
console.log(c || d); // true OR
console.log(!c); // true NOT

// ASIGNACIÓN
let e = 5;
e += 5; // e = e + 5
console.log(e); // 10

let f = 5;
f -= 5; // f = f - 5
console.log(f); // 0